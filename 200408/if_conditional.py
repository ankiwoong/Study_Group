# if
score = 70
if score >= 60:
    print('Pass')
print('-' * 30)


# if ~ else
score = 50
if score >= 60:
    print('Pass')
else:
    print('Fail')
print('-' * 30)

# if ~ elif ~ else
score = 50
if score >= 80:
    print('Pass')
elif score >= 50:
    print('Condition')
else:
    print('Fail')
print('-' * 30)
