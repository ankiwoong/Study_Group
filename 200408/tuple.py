# Sequence Structure Container(시퀀스 구조 컨테이너)
# empty tuple
a = ()
print(a)
print('-' * 30)

# tuple packing
alphabet = 'b', 'c', 'd', 'e'
print(alphabet)
print('-' * 30)

# tuple unpacking
f, g, h, i = 10, 20, 30, 40
print(f, g, h, i)
print('-' * 30)

# list -> tuple
j = ['indigo', 'Central']
print(tuple(j))
print('-' * 30)
